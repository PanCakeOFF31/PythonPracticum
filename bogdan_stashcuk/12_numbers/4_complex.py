    complex_b = 4 + 7j
    print(complex_a, complex_b, complex_a + complex_b)
    print(complex_a, complex_b, complex_a - complex_b)
    print(complex_a, complex_b, complex_a * complex_b)
    print(complex_a, complex_b, complex_a / complex_b)


